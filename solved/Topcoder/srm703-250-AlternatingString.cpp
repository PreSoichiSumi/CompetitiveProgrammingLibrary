class AlternatingString {
public:
    int maxLength(string s) {
        int it=0;
        int res=1;
        int maxRes=1;
        while(it<s.length()){
            chmax(maxRes,res);
            if((it+1)>=s.length()){
                res++;
                break;
            }
            if(s[it]!=s[it+1]){
                res++;
            }else{
                res=1;
            }
            it++;
        }
        return maxRes;
    }
};


